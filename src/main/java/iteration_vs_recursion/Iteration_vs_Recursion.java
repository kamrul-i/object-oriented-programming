package iteration_vs_recursion;

public class Iteration_vs_Recursion {

    /*
    Difference between iteration & recursion.
    
    Iteration:
    Iteration এর ক্ষেত্রে (for, while, do-while loop) ব্যবহার করা হয়,
    Terminates when continuation condition fails.
    Iteration is fast.
    Iteration code is bigger.
    
    Recursion:
    Recursion এর ক্ষেত্রে (if-else,switch) ব্যবহার করা হয়,
    Terminates when base case is satisfied.
    recursion is slow.
    recursion code is smaller.
    

     */
}
